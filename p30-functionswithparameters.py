def greet_user(name):
    print(f"Hi {name}")
    print("Welcome Buddy")


print("Start")
greet_user("Dhruvin")
greet_user("Diya")
print("Finish")

def greet_user(firstname, lastname):
    print(f"Hi {firstname} {lastname}")
    print("Welcome Buddy")


print("Start")
greet_user("Dhruvin", "Soni")
greet_user("Diya", "Soni")
print("Finish")